export function _sum(input) {
  let t1 = input["x"];
  let t2 = input["y"];
  let t3 = t1 + t2;
  return t3;
}

export function _product(input) {
  let t4 = input["x"];
  let t5 = input["y"];
  let t6 = t4 * t5;
  return t6;
}

export function _difference(input) {
  let t7 = input["x"];
  let t8 = input["y"];
  let t9 = t7 - t8;
  return t9;
}

export function _results_array(input) {
  let t11 = input["x"];
  let t13 = t11 + 10;
  let t14 = input["y"];
  let t16 = t14 * 2;
  let t17 = [1, t13, t16];
  return t17;
}

